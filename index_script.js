/* Team Member's Bio Buttons */

/* Mentor 1 */
var mentor1 = document.getElementById("mentor1_ID");
var mentor1_btn = document.getElementById("mentor1_btnID");

mentor1_btn.onclick = function() {
    mentor1.style.display = "block";
}

// Get the <span> element that closes the modal
var span1 = document.getElementsByClassName("close1")[0];

// When the user clicks on <span> (x), close the modal
span1.onclick = function() {
    mentor1.style.display = "none";
}

// When the user clicks anywhere outside of the modal, close it
window.onclick = function(event) {
  if (event.target == mentor1) {
    mentor1.style.display = "none";
  }
}


